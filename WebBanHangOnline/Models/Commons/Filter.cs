            Console.WriteLine(result);
        }

        public static string FilterChar(string input)
        {
            // Chuyển chuỗi thành chữ thường và loại bỏ dấu
            string normalizedString = RemoveDiacritics(input.ToLowerInvariant());

            // Thay thế các khoảng trắng bằng dấu gạch
            string result = normalizedString.Replace(" ", "-");

            return result;
        }

        public static string RemoveDiacritics(string text)
        {
            string formD = text.Normalize(NormalizationForm.FormD);
            StringBuilder sb = new StringBuilder();

            foreach (char ch in formD)
            {
                UnicodeCategory uc = CharUnicodeInfo.GetUnicodeCategory(ch);
                if (uc != UnicodeCategory.NonSpacingMark)
                {
                    sb.Append(ch);
                }
            }

            return sb.ToString().Normalize(NormalizationForm.FormC);
        }

